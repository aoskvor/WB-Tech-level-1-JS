function analyzePasswordStrength(password) {
    var strength = 0;
    var suggestions = [];
  
    // Проверка длины пароля
    if (password.length < 8) {
      suggestions.push("Пароль должен содержать не менее 8 символов");
    } else {
      strength += 1; // Увеличить силу, если длина пароля превышает 8 символов
    }
  
    // Проверка наличия цифр
    if (/\d/.test(password)) {
      strength += 1; // Увеличить силу, если есть цифры
    } else {
      suggestions.push("Рекомендуется добавить цифры в пароль");
    }
  
    // Проверка наличия символов в разных регистрах
    if (/[a-z]/.test(password) && /[A-Z]/.test(password)) {
      strength += 1; // Увеличить силу, если есть символы в верхнем и нижнем регистрах
    } else {
      suggestions.push("Рекомендуется использовать символы в разных регистрах");
    }
  
    // Проверка наличия специальных символов
    if (/[!@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]/.test(password)) {
      strength += 1; // Увеличить силу, если есть специальные символы
    } else {
      suggestions.push("Рекомендуется использовать специальные символы");
    }
  
    // Вывод оценки сложности пароля и предложений по улучшению
    if (strength === 0) {
      console.log("Оценка сложности пароля: Очень слабый");
    } else if (strength === 1) {
      console.log("Оценка сложности пароля: Слабый");
    } else if (strength === 2) {
      console.log("Оценка сложности пароля: Умеренный");
    } else if (strength === 3) {
      console.log("Оценка сложности пароля: Сильный");
    } else if (strength === 4) {
      console.log("Оценка сложности пароля: Очень сильный");
    }
  
    console.log("Предложения по улучшению: " + suggestions.join(", "));
  }
  
  // Пример использования функции
  var password = "P@ssw0rd";
  analyzePasswordStrength(password);

  
 // Этот код проверяет длину пароля, наличие цифр, символов в разных регистрах и специальных символов. Он также предлагает улучшения, если пароль не соответствует требованиям.
  